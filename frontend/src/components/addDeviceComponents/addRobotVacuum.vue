<template>
  <div>
    <div class="text-h4 q-mb-md">Робот-пылесос</div>

    <q-input
      v-model="name"
      filled
      label="Имя устройства"
      hint="Введите имя устройства"
      style="width: 99%"
    />

    <div style="height: 20px" />

    <q-select
      v-model="real_estate_picker"
      filled
      label="Помещение"
      hint="Выберите помещение, где находится устройство"
      :options="options"
      style="width: 99%"
    />

    <div style="height: 20px" />

    <q-input
      v-model="ip"
      filled
      label="IP-адрес"
      hint="Введите сетевой адрес устройства"
      style="width: 99%"
    />

    <div style="height: 20px" />

    <q-input
      v-model="password"
      filled
      label="Пароль"
      hint="Введите пароль доступа. Он должен содержать как минимум 1 строчный символ,
            1 заглавный символ, 1 цифру и быть не менее 8 символов в длину."
      :type="isPwd ? 'password' : 'text'"
      style="width: 99%"
    >
      <template v-slot:append>
        <q-icon
          :name="isPwd ? 'visibility_off' : 'visibility'"
          class="cursor-pointer"
          @click="isPwd = !isPwd"
        />
      </template>
    </q-input>

    <div style="height: 20px" />

    <q-input
      v-model="speed"
      filled
      label="Скорость движения"
      hint="Введите максимальную скорость движения"
      style="width: 99%"
    />

    <div style="height: 20px" />

    <q-input
      v-model="max_value"
      filled
      label="Максимальное расстояние от базовой станции"
      hint="Введите максимальное расстояние работы робота-пылесоса от базовой станции"
      style="width: 99%"
    />

    <div style="height: 20px" />

    <q-btn
      outline
      rounded
      color="primary"
      label="Добавить"
      @click="buttonClick"
    />
  </div>
</template>

<script>
import axios from "axios";
import passwordHash from "password-hash";

export default {
  data() {
    return {
      name: "",

      real_estate_picker: null,
      options: [],
      full_options: [],

      ip: "10.90.90.90",

      password: "",
      isPwd: true,

      speed: "",
      max_value: "",

      getData() {
        axios
          .get("http://localhost:13491/api/get/real_estates")
          .then(res => {
            const data = res["data"][0];

            data.forEach(element => {
              this.options.push(element[0][1]);

              this.full_options.push({
                id: element[0][0],
                name: element[0][1]
              });
            });
          })
          .catch(e => {
            this.$q.notify({
              message: "Не удалось получить доступ к базе данных: " + e,
              color: "negative"
            });
          });
      },

      pushData() {
        // Check name to protect from SQLi
        if (/^[a-zA-Zа-яА-Я0-9\-_]+$/.test(this.name) === false) {
          this.$q.notify({
            message: "Введенное имя некорректно!",
            color: "warning"
          });
          return;
        }

        // Check if real estate is picked
        if (this.real_estate_picker === null) {
          this.$q.notify({
            message: "Выберите помещение!",
            color: "warning"
          });
          return;
        }

        // Get real estate id by name
        let real_estate_id;
        this.full_options.forEach(element => {
          if (element["name"] === this.real_estate_picker) {
            real_estate_id = element["id"];
          }
        });

        // Check if IP is valid
        if (
          /^(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$/.test(
            this.ip
          ) === false
        ) {
          this.$q.notify({
            message: "Введенный IP-адрес некорректен!",
            color: "warning"
          });
          return;
        }

        // Check if password is strong
        if (
          /^(?=.*\d)(?=.*[a-z])(?=.*[A-Z])[0-9a-zA-Z]{8,}$/.test(
            this.password
          ) === false
        ) {
          this.$q.notify({
            message: "Пароль не удовлетворяет политике безопасности паролей!",
            color: "warning"
          });
          return;
        }

        // Check if speed is valid
        if (isNaN(this.speed) === true || this.speed < 0 || this.speed > 30) {
          this.$q.notify({
            message: "Введена некорректная скорость!",
            color: "warning"
          });
          return;
        }

        // Check if max_value is valid
        if (
          isNaN(this.max_value) === true ||
          this.max_value < 0 ||
          this.max_value > 30
        ) {
          this.$q.notify({
            message: "Введена некорректное расстояние от базовой станции!",
            color: "warning"
          });
          return;
        }

        // Get current time
        const date = new Date();
        const time =
          date.getFullYear() +
          "-" +
          (date.getMonth() + 1) +
          "-" +
          date.getDate() +
          " " +
          date.getHours() +
          ":" +
          date.getMinutes() +
          ":" +
          date.getSeconds();

        axios
          .post("http://localhost:13491/api/set/device", {
            device_type_id: 3,
            real_estate_id: real_estate_id,
            status: 0,
            time_activated: time,
            time_deactivated: time,
            name: this.name,
            ip: this.ip,
            to_alarm: null,
            temperature: null,
            humidity: null,
            speed: this.speed,
            color: null,
            max_value: this.max_value,
            power: 2100,
            password: passwordHash.generate(this.password)
          })
          .then(() => {
            this.$q.notify({
              message: "Устройство успешно добавлено!",
              color: "positive"
            });
          })
          .catch(e => {
            this.$q.notify({
              message: "Не удалось получить доступ к базе данных: " + e,
              color: "negative"
            });
          });
      }
    };
  },
  methods: {
    buttonClick() {
      this.pushData();
    }
  },
  created() {
    this.getData();
  }
};
</script>
